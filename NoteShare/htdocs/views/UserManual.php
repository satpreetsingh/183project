<B>Finding and Reading Posted Notes</B>
<BR />
From either the course session or study group homepage, find the section named 'Course Notes' or 'Group Notes'.  If you wish to view the notes, simply left click on any of the hyperlinked-enabled titles.  Only the last five postings will be displayed on this page, but if you would like to view all the postings, click on the link '<A HREF="http://apps.facebook.com/notesharesep/views/SessionNotes.php?ns_session=2" TARGET="_top">View all Notes</A>'.  


<P><BR></P>


<B>Uploading Your Own Notes</B>
<BR />
From the course session or study group homepage, click on the link labeled '<A HREF="http://apps.facebook.com/notesharesep/views/NewNote.php?ns_session=2" target="_top" class="fbFont left">Add New Note Set</A>' or from the expanded notes page, click on '</A> <A HREF="http://apps.facebook.com/notesharesep/views/NewNote.php?ns_session=2" TARGET="_top">Add New Note Set</A>'.  Enter a subject (limit of 45 characters), body (limit of 100 characters), and path to the uploaded note file.  The note file must be under 5 MB and be in either:  PS, ZIP, DVI, TXT, DOC, DOCX, XLS, XLSX, PPT, PPTX, JPEG, or PNG format.  To upload the file, click the 'Post Notes' button, otherwise to cancel click 'Cancel'.


<P><BR></P>


<B>Deleting Your Own Notes</B> 
<BR />
From the course session, study group homepage, or expanded notes page click on the '[X]' link on the same line as the note header.  Next, a pop up window will appear, click 'OK' to finalize deleting the note. 



